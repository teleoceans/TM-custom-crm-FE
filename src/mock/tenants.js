const createTenantDetail = ({
  id,
  name,
  phone,
  whatsappNumber,
  email,
  contractDate,
  industry,
  streetAddress1,
  streetAddress2,
  country,
  city,
  state,
  zipCode,
  timeZone,
  currency,
  website,
  status,
  assignedUsers = [],
  coldCallers = [],
  acquisitionManagers = [],
  dispositions = [],
}) => {
  return {
    id,
    profile: {
      name,
      phone,
      whatsappNumber,
      email,
      contractDate,
      industry,
      streetAddress1,
      streetAddress2,
      country,
      city,
      state,
      zipCode,
      timeZone,
      currency,
      website,
      status,
      assignedUsers: Array.isArray(assignedUsers) ? [...assignedUsers] : [],
      coldCallers: Array.isArray(coldCallers) ? [...coldCallers] : [],
      acquisitionManagers: Array.isArray(acquisitionManagers)
        ? [...acquisitionManagers]
        : [],
      dispositions: Array.isArray(dispositions) ? [...dispositions] : [],
    },
  };
};

